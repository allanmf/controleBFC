package barrosfilhos.controle.model;

/**
 *
 * @author allan
 */
public class PdfDas {

    private int idDas;
    private String compararLinha;
    private String cnpjDas;
    private String apuracaoDas;
    private String vencimentoDas;
    private String valorDas;

    public int getIdDas() {
        return idDas;
    }

    public void setIdDas(int idDas) {
        this.idDas = idDas;
    }

    public String getCompararLinha() {
        return compararLinha;
    }

    public void setCompararLinha(String compararLinha) {
        this.compararLinha = compararLinha;
    }

    public String getCnpjDas() {
        return cnpjDas;
    }

    public void setCnpjDas(String cnpjDas) {
        this.cnpjDas = cnpjDas;
    }

    public String getApuracaoDas() {
        return apuracaoDas;
    }

    public void setApuracaoDas(String apuracaoDas) {
        this.apuracaoDas = apuracaoDas;
    }

    public String getVencimentoDas() {
        return vencimentoDas;
    }

    public void setVencimentoDas(String vencimentoDas) {
        this.vencimentoDas = vencimentoDas;
    }

    public String getValorDas() {
        return valorDas;
    }

    public void setValorDas(String valorDas) {
        this.valorDas = valorDas;
    }


}


