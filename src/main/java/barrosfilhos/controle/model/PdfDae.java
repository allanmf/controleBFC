public class PdfDae {
 
private int idDae;
private String compararLinha;
private String cpfDae;
private String apuracaoDae;
private String vencimentoDae;
private String valorDae;

    public int getIdDae() {
        return idDae;
    }

    public void setIdDae(int idDae) {
        this.idDae = idDae;
    }

    public String getCompararLinha() {
        return compararLinha;
    }

    public void setCompararLinha(String compararLinha) {
        this.compararLinha = compararLinha;
    }

    public String getCpfDae() {
        return cpfDae;
    }

    public void setCpfDae(String cpfDae) {
        this.cpfDae = cpfDae;
    }

    public String getApuracaoDae() {
        return apuracaoDae;
    }

    public void setApuracaoDae(String apuracaoDae) {
        this.apuracaoDae = apuracaoDae;
    }

    public String getVencimentoDae() {
        return vencimentoDae;
    }

    public void setVencimentoDae(String vencimentoDae) {
        this.vencimentoDae = vencimentoDae;
    }

    public String getValorDae() {
        return valorDae;
    }

    public void setValorDae(String valorDae) {
        this.valorDae = valorDae;
    }



    
}
